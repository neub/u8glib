void draw_logo()
{
	u8g_uint_t x,y;
	u8g_uint_t w=logo_7s_width,h=logo_7s_height;
	u8g_uint_t val;
	int tmp;
	for(y=0;y<h;y++) {
		for(x=0;x<w;x++)
		{
			tmp=(y*w+x);
			val=logo_7s_bits[tmp/8];
			if(val>0 && (val & (1 << (tmp%8))))
			{
				u8g_DrawPixel(&u8g,x,y);
			}
		}
	}
}

void draw_url(void)
{
  u8g_SetFont(&u8g, u8g_font_4x6);
  u8g_DrawStr(&u8g, 6,58,"www.sevensols.com");
}


void draw(void) 
{
  draw_logo();
  draw_url();
}

int main(int argc, const char *argv[])
{
  u8g_linux_Init(&u8g, argc, argv);
  u8g_FirstPage(&u8g);
  do
  {
    draw();
  } while ( u8g_NextPage(&u8g) );

  return 0;
}

